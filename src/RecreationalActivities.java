public class RecreationalActivities {
private String name;
private double duration;

    public void setName(String name){
        this.name = name;
    }
    public String getName()
    {
        return name;
    }

    public void setDuration(double duration){
        this.duration = duration;
    }
    public double getDuration()
    {
        return duration;
    }
}
